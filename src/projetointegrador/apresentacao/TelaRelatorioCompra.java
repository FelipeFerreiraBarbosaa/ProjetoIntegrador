    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnImprimirActionPerformed
    {//GEN-HEADEREND:event_btnImprimirActionPerformed
        RelatorioCompra rel = new RelatorioCompra();
        ReportManager rm = new ReportManager();
        rel.setData1((Date) this.tfData1.getValue());
        rel.setData2((Date)this.tfData2.getValue() );
        
        try
        {
            rm.setDataBaseConnection(HibernateUtil.getConnection());
        } catch (Exception ex)
        {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            rm.createReport(rel);
        } catch (HibernateException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dispose();
    }//GEN-LAST:event_btnImprimirActionPerformed

