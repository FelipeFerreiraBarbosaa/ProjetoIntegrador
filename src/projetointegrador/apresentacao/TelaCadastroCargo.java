    public boolean isFilled () {
        
        //Método para verificar se todos os campos foram preenchidos, retorna true caso sim
        
        List<javax.swing.JTextField> lista = Arrays.asList(tfDesc, tfNome); //Adicionar campos para verificação aqui
        boolean isFilled = false;
        for (int i = 0; i < lista.size(); i++) {
            if (!lista.get(i).getText().equals("")) {
                isFilled = true;
            } else {
                isFilled = false;
            }
        }
        
        return isFilled;
        
    }
    
    public void setComboBox (javax.swing.JComboBox<String> comboBox, int[] itens) {
        
        //Método para popular o(s) combo box
        
        cbPerms.removeAllItems();        
        
        for (int i = 0; i < itens.length; i++) {
            comboBox.addItem(itens[i]+"");
        }
    }


