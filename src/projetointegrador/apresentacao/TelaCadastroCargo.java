    public void carregarTabela() throws DataBaseException {
        
        //Método para carregar ou atualizar a tabela de usuarios        
        
        DaoGenerico dao = new DaoGenerico();
        List<Cargos> lista = dao.read(Cargos.class, "FROM Cargos");

        CargosTableModel tm = new CargosTableModel(lista);
        tabela.setModel(tm);

        //Códigos para permitir a seleção na tabela
        ListSelectionModel selectionModel = tabela.getSelectionModel();
        tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        selectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {

                int row = tabela.getSelectedRow();

                if (row >= 0) {
                    CargosTableModel tableModel = (CargosTableModel) tabela.getModel();
                    cargoSelecionado = tableModel.getListaUsuario().get(tabela.getSelectedRow());
                    setLayout (false, true, true, true, true);
                }
            }
        });
        
    }
    
